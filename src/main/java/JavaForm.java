    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
          //Editar
        umlivro.setTitulo(txtTitulo.getText());
        umlivro.setAutor(txtAutor.getText());
        umlivro.setEditora(txtEditora.getText());
        umlivro.setAnoEdicao(txtAno.getText());
        umlivro.setLocalizacao(txtLoc.getText());
        LivroBLL.validaDadosEditar(umlivro,txtID.getText());
        if (Erro.getErro())
            javax.swing.JOptionPane.showMessageDialog(null,Erro.getMens());
        else
            javax.swing.JOptionPane.showMessageDialog(null,"Dados Editados com sucesso!");
             LivroDAL.CarregarTable(tblLivro);
    }//GEN-LAST:event_btEditarActionPerformed

    private void BtDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtDeletarActionPerformed
          LivroBLL.ValidaDadosDeleta(txtID.getText());
        if (Erro.getErro())
            javax.swing.JOptionPane.showMessageDialog(null,Erro.getMens());
        else
            javax.swing.JOptionPane.showMessageDialog(null,"Dados Deletados com sucesso!");
             LivroDAL.CarregarTable(tblLivro);
    }//GEN-LAST:event_BtDeletarActionPerformed
    
