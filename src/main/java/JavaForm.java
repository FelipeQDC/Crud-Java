    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        //Inserir
        umlivro.setTitulo(txtTitulo.getText());
        umlivro.setAutor(txtAutor.getText());
        umlivro.setEditora(txtEditora.getText());
        umlivro.setAnoEdicao(txtAno.getText());
        umlivro.setLocalizacao(txtLoc.getText());
        LivroBLL.validaDadosInseri(umlivro);
        if (Erro.getErro())
            javax.swing.JOptionPane.showMessageDialog(null,Erro.getMens());
        else
            javax.swing.JOptionPane.showMessageDialog(null,"Dados inseridos com sucesso!");
             LivroDAL.CarregarTable(tblLivro);
    }//GEN-LAST:event_btAdicionarActionPerformed

